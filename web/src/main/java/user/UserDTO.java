package user;

public class UserDTO {
	
	private String userID;
	private String userPW;
	private String userPWC;
	private String userName;
	private int userBirthYear;
	private int userBirthMonth;
	private int userBirthDate;
	private String userEmail;
	private String userGender;
	private String userProfile;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPW() {
		return userPW;
	}
	public void setUserPW(String userPW) {
		this.userPW = userPW;
	}
	public String getUserPWC() {
		return userPWC;
	}
	public void setUserPWC(String userPWC) {
		this.userPWC = userPWC;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getUserBirthYear() {
		return userBirthYear;
	}
	public void setUserBirthYear(int userBirthYear) {
		this.userBirthYear = userBirthYear;
	}
	public int getUserBirthMonth() {
		return userBirthMonth;
	}
	public void setUserBirthMonth(int userBirthMonth) {
		this.userBirthMonth = userBirthMonth;
	}
	public int getUserBirthDate() {
		return userBirthDate;
	}
	public void setUserBirthDate(int userBirthDate) {
		this.userBirthDate = userBirthDate;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserProfile() {
		return userProfile;
	}
	public void setUserProfile(String userProfile) {
		this.userProfile = userProfile;
	}

	
	
}
